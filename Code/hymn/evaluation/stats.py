"""Statistics over the long-format results: Mean/Median/Std/P75/P95 per (tech, method)."""
import os
import pandas as pd
import numpy as np

from hymn.config import OUT_DIR, EVAL


def compute_stats(long_df, techs=None):
    if techs is None:
        techs = EVAL["technologies"]
    df = long_df[long_df["technology"].isin(techs)].dropna(subset=["error"])
    rows = []
    for (tech, method), g in df.groupby(["technology", "method"]):
        rows.append({
            "technology": tech,
            "method": method,
            "n": int(len(g)),
            "mean": float(g["error"].mean()),
            "median": float(g["error"].median()),
            "std": float(g["error"].std(ddof=1)),
            "p75": float(g["error"].quantile(0.75)),
            "p95": float(g["error"].quantile(0.95)),
        })
    out = pd.DataFrame(rows)
    method_order = ["ILS", "RLS", "BGF", "ResNet-SpatialHoldout", "ResNet-RandomSplit"]
    out["technology"] = pd.Categorical(out["technology"], techs, ordered=True)
    out["method"] = pd.Categorical(out["method"], method_order, ordered=True)
    return out.sort_values(["technology", "method"]).reset_index(drop=True)


def write_stats(long_df, techs=None):
    stats = compute_stats(long_df, techs=techs)
    csv_path = os.path.join(OUT_DIR, "stats_table.csv")
    stats.to_csv(csv_path, index=False)
    tex_path = os.path.join(OUT_DIR, "stats_table.tex")
    _to_latex(stats, tex_path)
    return stats


def _to_latex(stats, path):
    metrics = ["mean", "median", "std", "p75", "p95"]
    tech_best = {}
    for tech, g in stats.groupby("technology", observed=True):
        best = {m: g[m].min() for m in metrics}
        tech_best[tech] = best

    lines = [
        "% auto-generated by evaluation/stats.py",
        "\\begin{tabular}{lrrrrrr}",
        "\\toprule",
        "Method & $n$ & Mean [m] & Median [m] & Std [m] & P75 [m] & P95 [m] \\\\",
        "\\midrule",
    ]
    prev_tech = None
    for _, r in stats.iterrows():
        tech = str(r["technology"])
        if prev_tech is not None and tech != prev_tech:
            lines.append("\\midrule")
        vals = []
        for m in metrics:
            v = r[m]
            s = f"{v:.3f}"
            if np.isclose(v, tech_best[r["technology"]][m]):
                s = f"\\textbf{{{s}}}"
            vals.append(s)
        method_label = str(r["method"])
        lines.append(
            f"{method_label} & {int(r['n'])} & "
            + " & ".join(vals) + " \\\\"
        )
        prev_tech = tech
    lines += ["\\bottomrule", "\\end{tabular}"]
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")


def compute_ranging_stats(rng_df, techs=("ble", "uwb", "wifi")):
    """Per-technology stats on absolute ranging residual."""
    df = rng_df[rng_df["technology"].isin(techs)].dropna(subset=["abs_residual"])
    rows = []
    for tech, g in df.groupby("technology"):
        rows.append({
            "technology": tech,
            "n": int(len(g)),
            "mean": float(g["abs_residual"].mean()),
            "median": float(g["abs_residual"].median()),
            "std": float(g["abs_residual"].std(ddof=1)),
            "p75": float(g["abs_residual"].quantile(0.75)),
            "p95": float(g["abs_residual"].quantile(0.95)),
        })
    out = pd.DataFrame(rows)
    out["technology"] = pd.Categorical(out["technology"], list(techs), ordered=True)
    return out.sort_values("technology").reset_index(drop=True)


def write_ranging_stats(rng_df, techs=("ble", "uwb", "wifi")):
    stats = compute_ranging_stats(rng_df, techs=techs)
    csv_path = os.path.join(OUT_DIR, "ranging_stats.csv")
    stats.to_csv(csv_path, index=False)
    tex_path = os.path.join(OUT_DIR, "ranging_stats.tex")
    _ranging_to_latex(stats, tex_path)
    return stats


def _ranging_to_latex(stats, path):
    metrics = ["mean", "median", "std", "p75", "p95"]
    best = {m: stats[m].min() for m in metrics}
    lines = [
        "% auto-generated by evaluation/stats.py",
        "\\begin{tabular}{lrrrrrr}",
        "\\toprule",
        "Technology & $n$ & Mean [m] & Median [m] & Std [m] & P75 [m] & P95 [m] \\\\",
        "\\midrule",
    ]
    for _, r in stats.iterrows():
        vals = []
        for m in metrics:
            v = r[m]
            s = f"{v:.3f}"
            if np.isclose(v, best[m]):
                s = f"\\textbf{{{s}}}"
            vals.append(s)
        lines.append(
            f"{str(r['technology']).upper()} & {int(r['n'])} & "
            + " & ".join(vals) + " \\\\"
        )
    lines += ["\\bottomrule", "\\end{tabular}"]
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")
